public class Date {
    public static void main(String[] args) {
        String day = "Friday";
        String date = "20";
        String month = "August";
        String year = "2021";

        System.out.println("American Format:");
        System.out.println(day + ", " + month + " " + date + ", " + year + "\n");
        System.out.println("European Format:");
        System.out.println(day + " " + date + " " + month + " " + year);
    }
}